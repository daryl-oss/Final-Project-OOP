    private void upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upActionPerformed
         String names = name.getText().trim();
    String lastt= last.getText().trim();
   String gends = (String) gender.getSelectedItem();
    String cor = (String) course.getSelectedItem();
     String Id = (String) id.getSelectedItem();

    if (names.isEmpty() || lastt.isEmpty() || gends.equals("")|| cor.equals("")|| Id == null) {
          JOptionPane.showMessageDialog(this, "ENTER THE DATA");
          return;
         }

    
int ids;

  try {
       ids=Integer.parseInt(Id);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numbers.");
        return;
    }

    try {
        connection.init();
        Connection c = connection.getConnection();
        String sql = "UPDATE tbl SET first_name = ?, last_name = ?, Gender = ?, Course = ? WHERE id = ?";
        PreparedStatement ps = c.prepareStatement(sql);
        ps.setString(1, names);
        ps.setString(2, lastt);
        ps.setString(3, gends);
        ps.setString(4, cor);
        ps.setInt(5, ids);

        int rows = ps.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Product updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "No product found with ID: " + id);
        }

        print(); 
        productid();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    }
    name.setText("");
   last.setText("");
    
    id.setSelectedItem(0);
    }//GEN-LAST:event_upActionPerformed

