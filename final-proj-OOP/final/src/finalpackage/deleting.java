    private void deletedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletedActionPerformed
         int selectedRow = table.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete.");
        return;
    }

    String id = model.getValueAt(selectedRow, 0).toString();

    int confirm = JOptionPane.showConfirmDialog(this, 
                    "Are you sure you want to delete this student?", 
                    "Confirm Deletion", 
                    JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try {
            connection.init();
            Connection c = connection.getConnection();

            String query = "DELETE FROM tbl WHERE id = ?";
            PreparedStatement ps = c.prepareStatement(query);
            ps.setString(1, id);

            int rowsAffected = ps.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Student deleted successfully.");
                print(); // refresh table
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete student.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(deleting.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_deletedActionPerformed

